#include "so_long.h"

void	find_player(t_so_long *p)
{
	p->i = 0;
	while (p->na[p->i])
	{
		p->j = 0;
		while (p->na[p->i][p->j])
		{
			if (p->na[p->i][p->j] == 'P')
				return ;
			p->j++;
		}
		p->i++;
	}
}
